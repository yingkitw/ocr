#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_render_bitmap_text() {
        let img = render_text_bitmap("AB", 64, Luma([255]), Luma([0]), 8);
        assert!(img.width() > 0);
        assert_eq!(img.height(), 64);
    }

    #[test]
    fn test_all_ascii_glyphs_exist() {
        let chars: Vec<char> = (' '..='~').collect();
        let mut missing = Vec::new();
        for ch in chars {
            if glyph_5x7_rows(ch).is_none() {
                missing.push(ch);
            }
        }
        assert!(missing.is_empty(), "Missing glyphs for: {:?}", missing);
    }
}
