//! Bitmap font renderer for synthetic text-line generation
//!
//! Provides a pure-Rust fallback when no TrueType fonts are available.
//! Uses embedded 5×7 bitmap glyphs for ASCII printable characters.

use image::{DynamicImage, GrayImage, Luma};

/// Render a text string as a bitmap image using embedded 5×7 glyphs
pub fn render_text_bitmap(
    text: &str,
    image_height: u32,
    background_color: Luma<u8>,
    text_color: Luma<u8>,
    padding: u32,
) -> DynamicImage {
    let glyph_width = 5u32;
    let glyph_height = 7u32;
    let scale = (image_height.saturating_sub(padding * 2) as f32 / glyph_height as f32).max(1.0) as u32;

    let scaled_glyph_w = glyph_width * scale;
    let scaled_glyph_h = glyph_height * scale;
    let text_width = text.len() as u32 * scaled_glyph_w;
    let img_width = text_width + padding * 2;
    let img_height = image_height;

    let mut image = GrayImage::from_pixel(img_width, img_height, background_color);

    let baseline_y = (img_height.saturating_sub(scaled_glyph_h)) / 2;

    for (char_idx, ch) in text.chars().enumerate() {
        let x_offset = padding + char_idx as u32 * scaled_glyph_w;
        if let Some(rows) = glyph_5x7_rows(ch) {
            for (row_idx, row) in rows.iter().enumerate() {
                for (col_idx, pixel) in row.chars().enumerate() {
                    if pixel == '1' {
                        for dy in 0..scale {
                            for dx in 0..scale {
                                let px = x_offset + col_idx as u32 * scale + dx;
                                let py = baseline_y + row_idx as u32 * scale + dy;
                                if px < img_width && py < img_height {
                                    image.put_pixel(px, py, text_color);
                                }
                            }
                        }
                    }
                }
            }
        }
    }

    DynamicImage::ImageLuma8(image)
}

